package Builder.Example2.view;

import java.util.Map;

/**
 * Created by Serhat CAN on 08.11.2015.
 */
public abstract class AddressView {

    // common attribute
    private String emailAddress;

    // PeopleAddress attributes
    private String firstName;
    private String lastName;
    private String company;
    private String phoneNumber;

    // GroupAddress attributes
    private String name;
    private String purpose;
    private Map<String, String> members; // email key, memberName is value

    public AddressView() {

    }

    public String getEmailAddress() {
        return emailAddress;
    }

    public void setEmailAddress(String emailAddress) {
        this.emailAddress = emailAddress;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public Map<String, String> getMembers() {
        return members;
    }

    public void setMembers(Map<String, String> members) {
        this.members = members;
    }

    public abstract void display();
}
